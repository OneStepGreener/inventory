// Storage key for language preference
const LANGUAGE_STORAGE_KEY = '@app_language_preference';

// Global language state
let currentLanguage = 'en'; // Default to English
let languageChangeListeners = [];
let isLanguageInitialized = false;

// Initialize language from storage
export const initializeLanguage = async () => {
  try {
    console.log('ğŸŒ Initializing language from storage...');
    const savedLanguage = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY);
    
    if (savedLanguage) {
      console.log('ğŸŒ Found saved language:', savedLanguage);
      currentLanguage = savedLanguage;
      
      // Notify all listeners
      setTimeout(() => {
        languageChangeListeners.forEach(listener => {
          console.log('ğŸ”” Notifying listener of loaded language');
          listener(savedLanguage);
        });
      }, 0);
    } else {
      console.log('ğŸŒ No saved language found, using default:', currentLanguage);
    }
    
    isLanguageInitialized = true;
  } catch (error) {
    console.error('ğŸŒ Error loading language from storage:', error);
    isLanguageInitialized = true;
  }
};

// Set current language and save to storage
export const setCurrentLanguage = async (lang) => {
  console.log('ğŸŒ Setting language to:', lang);
  currentLanguage = lang;
  console.log('ğŸŒ Current language is now:', currentLanguage);
  
  // Save to AsyncStorage
  try {
    await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, lang);
    console.log('ğŸŒ Language saved to storage successfully');
  } catch (error) {
    console.error('ğŸŒ Error saving language to storage:', error);
  }
  
  // Notify all listeners asynchronously to avoid setState during render
  setTimeout(() => {
    languageChangeListeners.forEach(listener => {
      console.log('ğŸ”” Notifying listener of language change');
      listener(lang);
    });
  }, 0);
};

// Get current language
export const getCurrentLanguage = () => {
  return currentLanguage;
};

// Check if language is initialized
export const isLanguageReady = () => {
  return isLanguageInitialized;
};

// Subscribe to language changes
export const subscribeToLanguageChanges = (listener) => {
  languageChangeListeners.push(listener);
  return () => {
    languageChangeListeners = languageChangeListeners.filter(l => l !== listener);
  };
};

// Get translation
export const t = (key) => {
  const translation = translations[currentLanguage]?.[key] || translations.en[key] || key;
  return translation;
};
